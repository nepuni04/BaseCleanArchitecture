        public static async Task SeedAsync(AppDbContext storeContext, ILoggerFactory loggerFactory)
        {
            try
            {
                await SeedProductTypesAsync(storeContext);
                await SeedProductsAsync(storeContext);
            }
            catch (Exception exception)
            {
                var log = loggerFactory.CreateLogger<AppDbContextSeed>();
                log.LogError(exception.Message);
                throw;
            }
        }

        private static async Task SeedProductTypesAsync(AppDbContext storeContext)
        {
            if (storeContext.ProductTypes.Any())
                return;

            var categories = new List<ProductType>()
            {
                new ProductType() { Name = "Laptop"}, // 1
                new ProductType() { Name = "Drone"}, // 2
                new ProductType() { Name = "TV & Audio"}, // 3
                new ProductType() { Name = "Phone & Tablet"}, // 4
                new ProductType() { Name = "Camera & Printer"}, // 5
                new ProductType() { Name = "Games"}, // 6
                new ProductType() { Name = "Accessories"}, // 7
                new ProductType() { Name = "Watch"}, // 8
                new ProductType() { Name = "Home & Kitchen Appliances"} // 9
            };

            foreach (var item in categories)
            {
                item.Created = DateTime.Now;
                item.LastModified = DateTime.Now;
            }

            storeContext.ProductTypes.AddRange(categories);
            await storeContext.SaveChangesAsync();
        }

